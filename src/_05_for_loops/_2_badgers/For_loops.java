package _05_for_loops._2_badgers;

public class For_loops {

	public static void main(String[] args) {
		for(int i = 0; i < 2;i++) {
			for(int k = 0; k < 12;k++) {
				System.out.print("Badger, ");
				
					
				
			}
				for(int j = 0; j < 2;j++) {
					System.out.print("Mushroom");
					if(j == 0) {
						System.out.print(", ");
					}
					if(j == 1) {
						System.out.println();
					}
				
				
				}
			
		}

		System.out.println("Snake!!");}
}
	

